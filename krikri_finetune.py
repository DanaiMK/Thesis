from trl import SFTConfig, SFTTrainer
trainer = SFTTrainer(
    model = model,
    tokenizer = tokenizer,
    train_dataset = dataset,
    dataset_text_field = "text",
    max_seq_length = max_seq_length,
    packing = False,
    args = SFTConfig(
        per_device_train_batch_size = 2,
        gradient_accumulation_steps = 4,
        warmup_steps = 10,
        num_train_epochs = 1,
        # max_steps = 60,
        # num_train_epochs = 1,  # full training: sees all 11,686 examples, e.g., for 3 epochs × 3 times (~4hrs on T4)
        # max_steps = 60,  # quick test: stops after 60 steps regardless of dataset size (~4min on T4)
        learning_rate = 2e-4,
        logging_steps = 10,
        optim = optim,
        weight_decay = 0.001,
        lr_scheduler_type = "linear",
        seed = 3407,
        output_dir = "respgen_outputs",
        report_to = "none",
    ),
)

trainer.train_dataset

