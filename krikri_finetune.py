"""## Data Preparation

Format our JSON into Alpaca-style text: `### Instruction / ### Input / ### Response`.
"""

from datasets import load_dataset
from unsloth.chat_templates import get_chat_template

# 1. Φορτώνουμε το template διαλόγου του Llama-3
tokenizer = get_chat_template(
    tokenizer,
    chat_template = "llama-3",
    mapping = {"role" : "role", "content" : "content", "user" : "user", "assistant" : "assistant"}
)

# 2. Συνάρτηση που μετατρέπει τους διαλόγους στο σωστό text format
def format_chat(examples):
    texts = [tokenizer.apply_chat_template(convo, tokenize=False, add_generation_prompt=False) for convo in examples["conversations"]]
    return {"text": texts}

# 3. Φορτώνουμε το αρχείο μας από το Google Drive
# ΠΡΟΣΟΧΗ: Αν το αρχείο σου είναι σε άλλο φάκελο στο Drive, άλλαξε το path!
data_path = "unsloth_dataset.json"

dataset = load_dataset("json", data_files={"train": data_path}, split="train")

# 4. Εφαρμόζουμε την αλλαγή σε όλο το dataset
dataset = dataset.map(format_chat, batched=True)

print(f"Loaded {len(dataset)} conversational examples successfully!")

dataset

print("Sample formatted text (first example):")
print(dataset[0]["text"])

"""## Train"""

# GPU memory before training
